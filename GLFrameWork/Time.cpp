#include "Polygon2D.h"
#include <math.h>
#include "Texture.h"
#include "Time.h"

CTime* Time[3] = { nullptr };
//=============================================================================
//コストラクタ
//=============================================================================
CTime::CTime()
{
	_Pos.x =
		_Pos.y =
		_Pos.z = 0;

	_Rot.x =
		_Rot.y =
		_Rot.z = 0;

	TimeCount = 0;
	Timer = 0;

	x =
		y = 0;
}
//=============================================================================
//初期化
//=============================================================================
void CTime::Init(void)
{
	CPolygon2D::Init();

	Texture = CTexture::Texture(TEX_NUMBER);
	SetUV(0, 0, 1.0f, 1.0f);

}
//=============================================================================
//作成
//=============================================================================
CTime* CTime::Create(const VECTOR3& pos, const VECTOR2& size, const VECTOR3& rot, const COLOR& color)
{

	for (int i = 0; i < 3; i++)
	{
		Time[i] = new CTime;
		Time[i]->_Pos = pos;
		Time[i]->_Pos.x += (i * 20.0f);
		Time[i]->_Rot = rot;
		Time[i]->SetSize(0, 0);
		Time[i]->_Color = color;
		Time[i]->Init();
	}

	return *Time;
}

//=============================================================================
//終了処理
//=============================================================================
void CTime::Uninit(void)
{
	delete this;
}
//=============================================================================
//更新
//=============================================================================
void CTime::Update(void)
{
	CPolygon2D::AddSize(x, y);
}
